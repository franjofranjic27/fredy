from typing import Annotated, Any

from pydantic import BaseModel, ConfigDict, Field, StringConstraints

NonEmptyStr = Annotated[str, StringConstraints(min_length=1)]


class EvalCase(BaseModel):
    """One golden dataset entry.

    The JSONL wire format uses camelCase keys and MUST stay stable — existing
    datasets generated by the former TypeScript service keep loading.
    Unknown extra keys are tolerated.
    """

    model_config = ConfigDict(populate_by_name=True, extra="allow")

    query_id: str = Field(alias="queryId", min_length=1)
    query: str = Field(min_length=1)
    relevant_chunk_ids: list[NonEmptyStr] = Field(alias="relevantChunkIds", min_length=1)
    source: str = Field(min_length=1)
    metadata: dict[str, Any] = Field(default_factory=dict)

    @property
    def relevant_ids(self) -> list[str]:
        return self.relevant_chunk_ids
